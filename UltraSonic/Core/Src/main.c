void HAL_TIM_IC_CaptureCallback(TIM_HandleTypeDef *htim)
{
    if(htim->Channel == HAL_TIM_ACTIVE_CHANNEL_1)
    {
        if(captureFlag== 0) //first value not capture
        {
            INC_Value1 = HAL_TIM_ReadCapturedValue(&htim3,TIM_CHANNEL_1); //read the first value
            captureFlag = 1;

            __HAL_TIM_SET_CAPTUREPOLARITY(htim,TIM_CHANNEL_1,TIM_INPUTCHANNELPOLARITY_FALLING);
        }
        else if(captureFlag==1) //first is already value
        {
            INC_Value2= HAL_TIM_ReadCapturedValue(&htim3, TIM_CHANNEL_1);
            __HAL_TIM_SET_COUNTER(htim,0);
            if(INC_Value2 >INC_Value1)// normal
            {
                echoTime = INC_Value2-INC_Value1;
            }
            else if(INC_Value1 > INC_Value2)
            {
                echoTime= (0xffff-INC_Value1)+ INC_Value2;
            }
            distance = echoTime / 58;
            captureFlag = 0;

            __HAL_TIM_SET_CAPTUREPOLARITY(htim,TIM_CHANNEL_1,TIM_INPUTCHANNELPOLARITY_RISING);
            __HAL_TIM_DISABLE_IT(&htim3, TIM_IT_CC1);
        }
    }
}



void HCSR04(void) // Trig pin 10us
{
    HAL_GPIO_WritePin(TRIG_PORT, TRIG_PIN, GPIO_PIN_SET);
    delay_us(10);
    HAL_GPIO_WritePin(TRIG_PORT, TRIG_PIN, GPIO_PIN_RESET);

    __HAL_TIM_ENABLE_IT(&htim3,TIM_IT_CC1);
}
